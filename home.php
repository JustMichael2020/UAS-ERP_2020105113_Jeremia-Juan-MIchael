<center><b><u><font Size="5">TRIVIA</font></center></u></b>
<div style="padding:6px;">
  <ul>
        <li>Monsters University's library is called Bohol Hall, named after Pixar artist Nelson Bohol who designed it.</li>
        <li>Unlike its rival Fear Tech (whose mascot is Archie the Scare Pig), it is unknown what Monsters University's sports mascot is, or even if they have any, given that they send Sulley to steal Archie from Fear Tech.</li>
        <li>Although we only see seven fraternities and sororities (four fraternities and three sororities), according to the Monsters University website, there are twelve in all, with the two fraternities now shown being EOP and GRR and the three sororities not shown being EKO, AEA, and XOA.</li>
        <li>The campus resembles that of the University of California at Berkeley, right down to the entrance gate and clock tower. UC Berkeley is the university that's closest to the Pixar headquarters.</li>
        <li>The School of Scaring Building is based on Hamerschlag Hall at Carnegie Mellon University. It also resembles the HP Lovecraft monster Chthulhu, with its green dome resembling its head (and its skylights as eyes), and the columns around the entrance (which are decorated with fang-like protrusions) resembling its mouth tentacles.</li>
        <li>The year that Monsters University was established, 1313, is a reference to the address to the Disneyland Resort in Anaheim, California (1313 Disneyland Drive, Anaheim, CA 92802), as well as the stereotypical fear of the number 13, also known as triskaidekaphobia.</li>
        <li>The university has its own theme song, titled the same.</li>
